import time

for i in range(10):
    print(i)

for i in range(10+1):
    print(i)

for i in range(10):
    print(i+1)

for i in range(20,30+1):
    print(i)
    
for i in range(20,30+1,5):
    print(i)

for i in "Kaoru":
    print(i)

for seconds in range(10,0,-1):
    print(seconds)
