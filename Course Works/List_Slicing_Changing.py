# Learning the slice operator (colon)

my_list = ['k','e','t','e','m','a','r','a','e']
# elements 4th to 7th
print(my_list[3:7])

# elements beginning to 4th
print(my_list[0:4])
print(my_list[:-5])

# elements 6th to end
print(my_list[5:])
print(my_list[-4:])

# elements beginning to end
print(my_list[:])
print(my_list)
      
