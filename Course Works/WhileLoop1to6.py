# This is a program with a while loop that prints 1 through to
# n in square brackets.
# ex) user enters 6
# then display
# [1][2]....

x = 1

n = print("Please enter a number you like: \n")
while x <= n:
    print("Your number ranges are ",[x], "\n")
    x += 1

